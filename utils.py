def seq_to_onehot(seq):
    onehot = []  
    for aa in seq:
        onehot.append(AA_TO_ONEHOT[aa])
    return np.array(onehot)

def variant_to_seq(variants_row, wild_type_seq, wild_type_startpos):
    tmp_wt = wild_type_seq
    variants = variants_row.split(",")
    for variant in variants:
        pos = int(variant[1:-1]) - wild_type_startpos
        old_aa = variant[0]
        new_aa = variant[-1]
        assert wild_type_seq[pos] == old_aa, "Variant does not match wild type sequence"
        tmp_wt = tmp_wt[:pos] + new_aa + tmp_wt[pos+1:]
    return tmp_wt

def variants_to_onehot_seq(variants_df, wild_type_seq, wild_type_startpos):
    onehot_seqs = []
    for variants_row in variants_df:
        seq = variant_to_seq(variants_row, wild_type_seq, wild_type_startpos)
        onehot_seqs.append(seq_to_onehot(seq))
    return np.array(onehot_seqs)

def variants_to_seq(variants_df, wild_type_seq, wild_type_startpos):
    seqs = []
    for variants_row in variants_df:
        seq = variant_to_seq(variants_row, wild_type_seq, wild_type_startpos)
        seqs.append(str(seq))
    return seqs